Game.Room = function (doors, floorPlan, randObstacles) {
  this.attr = {
    _doors: doors,
    _floorPlan: floorPlan,
    _randObstacles: randObstacles
  };
};

Game.Room.prototype.getDoors = function () {
  return this.attr._doors;
};

Game.Room.prototype.closeDoor = function (direction) {
  this.attr._doors[direction] = false;
};

Game.Room.prototype.hasOpenDoor = function () {
  return this.attr._doors.north || this.attr._doors.east || this.attr._doors.south || this.attr._doors.west;
};

Game.Room.prototype.clone = function () {
  return new Game.Room({north:this.attr._doors.north, east:this.attr._doors.east, south:this.attr._doors.south, west:this.attr._doors.west}, this.attr._floorPlan, this.attr._randObstacles);
};

Game.Room.prototype.checkGrid = function (roomGrid, x, y) {
  //If any rooms are adjacent to the proposed location, make sure their doors match the current rooms doors.
  if(roomGrid[x][y - 1] && (roomGrid[x][y - 1].getDoors().south != this.attr._doors.north)) {
    return false;
  } if(roomGrid[x + 1][y] && (roomGrid[x + 1][y].getDoors().west != this.attr._doors.east)) {
    return false;
  } if(roomGrid[x][y + 1] && (roomGrid[x][y + 1].getDoors().north != this.attr._doors.south)) {
    return false;
  } if(roomGrid[x - 1][y] && (roomGrid[x - 1][y].getDoors().east != this.attr._doors.west)) {
    return false;
  }
  //If the proposed location lies on any border, make sure it does not create a door along that border.
  if(y <= 0 && this.attr._doors.north) {
    return false;
  } else if(y >= 12 && this.attr._doors.south) {
    return false;
  } if(x <= 0 && this.attr._doors.west) {
    return false;
  } else if(x >= 12 && this.attr._doors.east) {
    return false;
  }
  return true;
};

Game.Room.prototype.closeAdjDoors = function (roomGrid, x, y) {
  if(this.attr._doors.north && roomGrid[x][y-1] && (roomGrid[x][y - 1].getDoors().south)) {
    roomGrid[x][y - 1].closeDoor('south');
    this.attr._doors.north = false;
  } if(this.attr._doors.east && roomGrid[x + 1][y] && (roomGrid[x + 1][y].getDoors().west)) {
    roomGrid[x + 1][y].closeDoor('west');
    this.attr._doors.east = false;
  } if(this.attr._doors.south && roomGrid[x][y + 1] && (roomGrid[x][y + 1].getDoors().north)) {
    roomGrid[x][y + 1].closeDoor('north');
    this.attr._doors.south = false;
  } if(this.attr._doors.west && roomGrid[x - 1][y] && (roomGrid[x - 1][y].getDoors().east)) {
    roomGrid[x - 1][y].closeDoor('east');
    this.attr._doors.west = false;
  }
};

Game.Room.prototype.writeToGrid = function (tilesGrid, x, y) {
  for (var column = 0; column < this.attr._floorPlan.length; column++) {
    for (var row = 0; row < this.attr._floorPlan[0].length; row++) {
      tilesGrid[x + column][y + row] = this.attr._floorPlan[column][row];
    }
  }

};
